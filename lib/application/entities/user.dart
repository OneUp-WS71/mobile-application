class User {
    final int id;
    final String username;
    final String password;
    final String name;
    final String lastname;
    final String email;
    final String phone;
    final String type;
    final String gender;

    User({
        required this.id,
        required this.username,
        required this.password,
        required this.name,
        required this.lastname,
        required this.email,
        required this.phone,
        required this.type,
        required this.gender,
    });
}